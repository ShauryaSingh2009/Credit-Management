$(document).ready(function(){
	$('#sub1').click(function(){
		$.ajax({
			url:'/detail/',
			type:'POST',
			data:{'id':1},
			success:function(response)
			{
				if(response=='success')
				{
					window.location.href='/details/'
				}
				if(response=='error')
				{
					window.alert("Hello")
					window.location.href='/'
				}
			},
			error:function(xhr,textStatus,thrownError)
			{
				window.location.href='/'
			}
		});
	});
});

