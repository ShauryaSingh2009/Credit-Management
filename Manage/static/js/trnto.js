$(document).ready(function(){
	$('#trn1').click(function(){
    var crdt=$('#crdt').val();
		$.ajax({
			url:'/trncrd/',
			type:'POST',
			data:{'id':1,'crdt':crdt},
			success:function(response)
			{
				if(response=='success')
				{
					window.location.href='/'
				}
				if(response=='error')
				{
					window.alert("Hello")
					window.location.href='/'
				}
			},
			error:function(xhr,textStatus,thrownError)
			{
				window.location.href='/'
			}
		});
	});
});

